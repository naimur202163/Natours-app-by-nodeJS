// 3rd MiddleWeare
app.route('/api/v1/tours').get(getAllTours).post(createTour);

app.route('/api/v1/tours/:id').get(getTour).patch(updateTour).delete(deletTour);
// 4 Start The Server
app.listen(port, () => {
  console.log(`app is runnning on port ${port}`);
});
